.macro  adrli   dest, addr, a, b, c
        .set    \a, \addr-.-8
        .set    \b, (( \a+256 )>>9)<<9
        add     \dest, pc, #\b
        .set    \c, \a-\b
        add     \dest, \dest, #\c
.endm

.macro  adrl    dest, addr
        adrli   \dest, \addr, "adrla\@", "adrlb\@", "adrlc\@"
.endm

	
